package quiz;

public class Quiz1 {

	public static void main(String[] args) {

		int x ;									// int 형 변수 x 선언
		x = 1;									// x 에 1을 저장
		x = 5;									// x 의 값을 5로 변경
		System.out.println(x);		// x 를 출력
		
		int y = 3; 							// 변수 y 를 선언하고 바로 3을 대입		
		y = 6;									// y의 값을 6으로 변경 
		System.out.println(y);		// y를 출력

	}

}
